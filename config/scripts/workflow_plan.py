#!/usr/bin/env python3
"""
workflow_plan.py — Plan 階段（任務拆解）

原則：每個任務必須是「可獨立驗證的最小單位」。
避免「大爆炸式」實作——每步完成後都應該能跑測試。
"""
import json, sys

def main():
    try:
        req = json.load(sys.stdin)
    except Exception:
        req = {}

    task = req.get("user_input", "（未提供任務描述）")

    print(f"""# Plan — 任務拆解

**規格**：{task}

---

## 拆解原則
- 每個任務 ≤ 2 小時
- 每個任務完成後可獨立驗證（跑測試、看輸出）
- 不要跳過——每步都要做，即使看起來很小

---

## 任務清單

### 🔴 Phase 1：基礎結構
- [ ] T-1.1：（例：定義資料結構 / 介面）
- [ ] T-1.2：（例：建立空的模組骨架）
- [ ] T-1.3：（例：加入基礎測試）
  - 驗證方式：`cargo test` 全綠

### 🟡 Phase 2：核心邏輯
- [ ] T-2.1：（例：實作主要業務邏輯）
- [ ] T-2.2：（例：處理邊界條件）
- [ ] T-2.3：（例：整合到現有模組）
  - 驗證方式：功能測試通過

### 🟢 Phase 3：完善與整合
- [ ] T-3.1：（例：錯誤處理）
- [ ] T-3.2：（例：UI / API 接線）
- [ ] T-3.3：（例：端對端測試）
  - 驗證方式：全部 AC 通過

---

## 依賴關係
```
T-1.1 → T-1.2 → T-2.1
T-1.3 可並行
T-3.x 全部依賴 Phase 2 完成
```

---

## 風險點
- ⚠️ 最複雜的部分：
- ⚠️ 最容易出錯的邊界：

---

**下一步** → 按任務順序進入 **Build**（一次只做一個任務）
""")

if __name__ == "__main__":
    main()
